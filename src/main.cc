#include <common.hh>
#include <expr.hh>
#include <model.hh>

#include    <smvLexer.h>
#include    <smvParser.h>

static void
parseFile(pANTLR3_UINT8 fName)
{
  pANTLR3_INPUT_STREAM input;
  pANTLR3_COMMON_TOKEN_STREAM tstream;

  psmvParser psr;
  psmvLexer  lxr;

  logger << "Preparing for parsing..." << endl;

  input = antlr3FileStreamNew(fName, ANTLR3_ENC_8BIT);
  assert(input);

  lxr = smvLexerNew(input); // smvLexerNew is generated by ANTLR
  assert(lxr);

  tstream = antlr3CommonTokenStreamSourceNew(ANTLR3_SIZE_HINT, TOKENSOURCE(lxr));
  assert(tstream);

  psr = smvParserNew(tstream);  // smvParserNew is generated by ANTLR3
  assert(psr);

  psr->smv(psr);
  logger << "done." << endl;

  psr->free(psr);
  tstream->free(tstream);
  lxr->free(lxr);
  input->close(input);
}

int main()
{
  parseFile((pANTLR3_UINT8)"test.smv");
  return 0;
}
